réponse du gouvernement
le charte canadien du droit et liberté
quatrième anniversaire de le proclamation
monsieur le président , notre gouvernement avoir prouver mon adhésion à ce important principe en prendre du mesure pour appliquer plus systématiquement le précepte de le charte .
je avoir prendre le initiative de évaluer et de modifier un loi et du politique en vigueur afin que je correspondre à un interprétation généreux de le charte .
ce faire , je avoir chercher à dépasser le option politique traditionnel pour me ouvrir à de nouveau perspective .
ainsi , en décembre dernier , je annoncer de nouveau initiative et le déblocage de crédit en vue de améliorer le sort du handicapé .
monsieur le président , je être fier du rôle que le canadien avoir jouer dans ce tâche difficile qui consister à définir le moyen à mettre en oeuvre pour réaliser le égalité et le justice social au canada .
je tenir ferme au processus de consultation , car je croire que le canadien avoir le droit de prendre part à le mise au point du politique qui me toucher de près .
je être indéniable que le charte toucher personnellement chacun de moi .
je obliger le tribunal et le gouvernement à rechercher constamment le équilibre entre le droit individuel et collectif de manière à favoriser le intérêt de le société sans pour autant brimer le droit du particulier .
le charte accorder au particulier de nouveau moyen de faire valoir notre droit si je estimer que je y avoir porter atteindre .
je imposer également à tout le tolérance à le égard du droit du autre .
